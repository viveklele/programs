class Class{
	
	String name;
	int age;
}	

class JavaClass{
	public static void main(String[] args){
	Class obj1 = new Class();
		obj1.name = "Jon";
		obj1.age = 100;
	System.out.print(obj1.name + ", ");
	System.out.println(obj1.age);	
		
	Class obj2 = new Class();
		obj2.name = "cave";
		obj2.age = 44;
	System.out.print(obj2.name + ", ");
	System.out.println(obj2.age);
	}	
}
	
	
	
